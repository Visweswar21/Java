import java.util.*;
public class switchcase {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		String s=sc.next();
		switch(s) {
		case "monday":
			System.out.println("sreedher fucks sumanth");
			break;
		case "tuesday":
			System.out.println("Lets us learn java");
			break;	
		case "wendesday":
			System.out.println("who fuck sreedhar ");
			break;
		default:
			System.out.println("no case is there");
			break;
		}
	}
}

	